	<h4>Questions for All Adopters:</h4>

<p>Do you have any other dogs and how will they react to a new dog?</p>
<p>Is your current residence suited to the dog you're considering?</p>
<p>How will your social life or work obligations affect your ability to care for a dog?</p>
<p>Do you have a plan for your new dog during vacations and/or work travel?</p>
<p>How do the people you live with feel about having a dog in the house?</p>
<p>Are you (or your spouse, partner or roommate) intolerant of hair, dirt and other realities of sharing your home with a dog, such as allergies?</p>
<p>Do you or any of your household/family members have health issues that may be affected by a dog?</p>
<p>What breed of dog is the best fit with your current lifestyle? (You can find information on specific breeds in our dog breed directory.)</p>
<p>Is there tension in the home? Dogs quickly pick up on stress in the home, and it can exacerbate their health and behavior problems.</p>
<p>Is there an adult in the family who has agreed to be ultimately responsible for the dog's care?</p>

<h4>Other Considerations:</h4>

<p>What do you expect your dog to contribute to your life? For example, do you want a running and hiking buddy, or is your idea of exercise watching it on TV?</p>
<p>If you are thinking of adopting a young dog, do you have the time and patience to work with the dog through its adolescence, taking house-breaking, chewing and energy-level into account? (Find more information on raising young dogs in our Puppy Guide.)</p>
<p>Have you considered your lifestyle carefully and determined whether a younger or older dog would be a better match for you?</p>
<p>Can you train and handle a dog with behavior issues or are you looking for an easy-going friend?</p>
<p>Do you need a dog who will be reliable with children or one you can take with you when you travel?</p>
<p>Do you want a dog who follows you all around the house or would you prefer a less clingy, more independent character?</p>

<h4>Size Considerations:</h4>

<h4>What size dog can your home accommodate?</h4>
<p>Will you have enough room if your dog grows to be bigger than expected?</p>
<p>What size dog would suit the other people who live in or visit your home regularly?</p>
<p>Do you have another dog to consider when choosing the size of your next dog?
How big a dog can you travel comfortably with?</p>

<h4>Dog Costs:</h4>

<p>More likely than not, the adopting agency will charge a fee to help defray the cost of taking in unwanted or lost animals. The adoption fee you pay will be a tiny fraction of the money you will spend over the life of your dog.</p>
<p>You may need to pay for your adopted dog to be spayed or neutered before bringing him or her home.</p>

<p>Some expenses are mandatory for all dogs, including:</p>
<ul>
<li>Food</li>
<li>Routine veterinary care</li>
<li>Licensing according to local regulations</li>
<li>Collars, leashes and identification tags</li>
<li>Kitty litter and box</li>
<li>Basic grooming equipment and supplies.</li>
 </ul>

<p>Other expenditures may not be required but are highly recommended:</p>
<ul>
<li>Permanent identification, such as a microchip or tattoo</li>
<li>Training classes</li>
<li>Additional grooming supplies or professional grooming (depending on your new dog's needs)</li>
<li>A spare collar or leash</li>
<li>A bed and toys</li>
<li>A crate or carrier</li>
</ul> 

<p>Unexpected costs: Accidents and illness can result in costly emergency veterinary care. Recovery tools for finding a missing dog can include posters and rewards.</p>
<p>A dog with special physical or behavioral challenges may require specialized professional support to overcome any obstacles these issues present.</p>
<p>For more on typical dog care costs visit Annual Dog Care Costs.</p>

<h4>Time Considerations:</h4>

<p>Dogs need to be fed two to three times a day, more often in the case of puppies, and need a constant supply of fresh water.</p>
<p>A responsible dog parent should spend at least one hour per day giving direct attention to his or her dog. This may include training, exercising, grooming, and playing or, with cats, may just be lap time on the couch. Dogs will need to be taken out to potty several times a day.</p>
<p>A dog with an abundance of energy needs more time to exercise and interactive toys to keep them entertained.</p>
<p>Dogs with long coats need 20 minutes a day of grooming to prevent matting.</p>
<p>Dogs with certain medical conditions may need additional attention, including specifically timed injections in the case of diabetic animals.</p>
<p>Remember that adopted dogs may need additional bonding and reassurance time in the early weeks.</p>

<h4>Shopping Checklist:</h4>

<p>It may be a good idea to wait until you select your new dog before you begin shopping for supplies. For example, some items, such as food and water bowls or collars and harnesses, depend upon the size of the dog you will be adopting.</p>

<p>Also, be sure to find out which food your dog was eating in the shelter or foster home so that you can provide the same in the beginning, again to ease the transition. After the dog has settled in, talk with your veterinarian about switching to the food of your choice.</p>

