				<h4>Snacks and treats</h4>

				<p>While pets who look at you with those soulful eyes can be irresistible when it comes to begging for a treat, snacks and treats are generally not designed to provide your pet with complete and balanced nutrition. Snacks and treats should be fed in conjunction with a complete and balanced diet. While an occasional treat is fine, snacks, treats and table scraps used in excess may unbalance a pet's diet. </p>

				<h4>Packaging and storage</h4>

				<p>When selecting a product off the shelves, make sure the bag is intact and secure: not torn, broken, dirty or damaged. All pet food should have a code date or provide a best if used by date - ask your local retailer to explain the date code if you have any questions.</p>

				<p>Once you have brought the product home, store the food in a dry, tightly sealed glass, metal or rigid plastic container. Improper storage increases the risk of loss palatability and nutritional value. Do not store the food near stoves, hot water pipes or heating ducts as heat and moisture can cause food to lose its quality more rapidly. Be on the alert for insect infestation, which can occur in any home. Discard the pet food if it is moldy or smells different than when you first brought it home, or if it becomes infested.</p>

