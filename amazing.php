			<h4>#5 Meet BeeBee</h4>
			<p>A Chihuahua named BeeBee was born July 4 with an abnormality to her front legs, making it difficult for her to walk. Born without any shoulder blades, she cannot put any weight on her legs or straighten them out.</p>

<p>She was also vulnerable to being bullied by her siblings. Her guardian, Denise Steininger told USA Today, "She got beat up a lot by her brothers and sister." It got so bad, Denise figured it might be best to bring BeeBee with her to the nursing home where she works. </p>
		<p>At the Life at Alpine Living Center in Thornton, Colorado, BeeBee has brought a ray of sunshine to residents there. <p>According to resident Maria Becerra, BeeBee is an inspiration, "If she can get through what she's going through, I know I can."</p>

<p>Denise knew BeeBee would need a means to get around and set about to get her a doggie wheelchair. But after consulting with her veterinarian, the only kind they could find were for rear legs.</p>

<p>That's when a co-worker of Denise's suggested Denise speak with the Bollman Technical Education Center, where her son is a pre-engineering student. After contacting the school, instructors thought it would be a great project for their students. </p>

<p>Hunter Freed, Justin Erickson, and Kyle Cary took on the student-community project. The three students filmed BeeBee and studied how BeeBee walked. They also took her measurements and then brainstormed together on designs to help the tiny dog in need. </p>
		
<p>When BeeBee first tried the prototype they had built, she had already grown a bit, so they needed to make some modifications. When that was done, Denise took the wheelchair home for BeeBee to practice on for a week. When she showed her veterinarian the device it won his approval.</p>

<p>It also won the approval of residents at the Alpine Living Center. Denise noticed how the people around the Center watched BeeBee and expressed their appreciation for the puppy who, like many of them, also uses a wheelchair.</p>

<p>With her newfound mobility, BeeBee joyously scoots around the hallways, full of puppy energy. Said Denise, "Bollman students gave my little BeeBee wings!" </p>

<p>Denise said BeeBee is "a special dog" and plans to get BeeBee approved to become a therapy dog for the facility. And thanks to the Bollman students, BeeBee gets around much more easily during her inspirational visits. Said Denise, "It worked out just perfectly." </p>


