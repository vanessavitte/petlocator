				<h4>Nail Clipping</h4>
				
				<p>Most people really don't handle their dog's feet until they are about to clip the nails and then... watch out! Some animals can get very upset at this totally foreign feeling. That's why it's a good idea to get your dog used to having her feet touched before you attempt a nail trim. Rub your hand up and down her leg and then gently press each individual toe-and be sure to give her lots of praise and some food treats as you do this. Every animal is different, but chances are that within a week or two of daily foot massage, your dog will be better able to tolerate a trim. Here's how to do it: </p>
			<ul>
				<li>Begin by spreading each of your dog's feet to inspect for dirt and debris. </li>
				<li>Use sharp, guillotine-type nail clippers to cut off the tip of each nail at a slight angle, just before the point where it begins to curve. </li>
				<li>Take care to avoid the quick, a vein that runs into the nail. This pink area can be seen through the nail. If your dog has black nails, however, the quick will not be as easily discernible, so be extra careful. </li>
				<li>If you do accidentally cut into the quick, it may bleed, in which case you can apply some styptic powder to stop the bleeding. </li>
				<li>Once the nails have been cut, use an emery board to smooth any rough edges.</li>
