				<h4>Brushing</h4>

				<p>Regular grooming with a brush or comb will help keep your pet's hair in good condition by removing dirt, spreading natural oils throughout her coat, preventing tangles and keeping her skin clean and irritant-free. And grooming time's a great time to check for fleas and flea dirt--those little black specks that indicate your pet is playing host to a flea family.</p>

