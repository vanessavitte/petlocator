				<h4>Bathing</h4>
				
				<p>The ASPCA recommends bathing your dog every 3 months or so; your pet may require more frequent baths in the summertime if she spends lots of time with your outdoors. Always use a mild shampoo that's safe to use on dogs, and follow these easy steps: </p>
			<ul>	
				<li>First, give your pet a good brushing to remove all dead hair and mats.</li>
				<li>Place a rubber bath mat in the bathtub to provide secure footing, and fill the tub with about 3 to 4 inches of lukewarm water. </li>
				<li>Use a spray hose to thoroughly wet your pet, taking care not to spray directly in her ears, eyes or nose. If you don't have a spray hose, a large plastic pitcher or unbreakable cup will do.  </li>
				<li>Gently massage in shampoo, working from head to tail.  </li>
				<li>Thoroughly rinse with a spray hose or pitcher; again, avoid the ears, eyes and nose.  </li>
				<li>Check the ears for any foul odors or excessive debris; if you choose to use a cleansing solution on a cotton ball, take care not to insert it into the ear canal. </li>
				<li>Dry your pet with a large towel or blow dryer, but carefully monitor the level of heat. </li>
