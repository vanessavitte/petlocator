<h4>PERSISTENCE, PATIENCE and REWARDS</h4>
 
<p>Toilet training is one of the biggest things you need to teach your puppy to ensure a smooth and happy transition into their new home. Before your new puppy arrives decide where you would like the designated toilet spot to be and make sure everyone is aware of it, toilet training needs to start from day one.</p>
 
<p>Be prepared that toilet training takes persistence and patience and it's your responsible to teach your puppy where they can go to the toilet.  The average age for a puppy to have full bladder control is around 18 weeks. You cannot expect a new puppy to go throughout the night without needing to go to the toilet.</p>
 
<h4>How do you minimise the mistakes?</h4>
 
<p>From day one you need to create a warm, safe and secure place for your puppy to sleep and spend time when you are unable to supervise them.   Popular places are the laundry, bathroom or garage, places that are easy to clean and cause less annoyance when those little accidents occur.  It also gives your puppy a place of their own and an area to relax.</p>
 
<p>When setting up the room for your puppy make sure it is as comfortable as possible, it doesn't need to be a big area, actually, a smaller area is probably best to start with.  Split the room into 3 small sections, one with the puppy's bed, an area for the puppy to go to the toilet, this can be papered, and an area in between with water bowl and place to play with toys. Puppies by nature do not like to eliminate in their beds or play areas so they will naturally gravitate to the papered area.  By setting up a room for your puppy you minimise the amount of mistakes they can make. If your puppy has the full run of the house how are you able to manage and monitor where they are going to the toilet?  By giving them a designated space you can focus on the positive and minimise the amount of unwanted accidents, giving your puppy a much better chance of success.  </p>
				
				<h4>Right... you are all set-up and ready to go, what now? </h4>
 
<p>Now you need to teach your puppy the right place to go to the toilet.  Unfortunately new puppies do not come with a flashing light that alerts us to their need for the toilet so you will have to work with your puppy as you learn their habits.  A general rule is to take your puppy out to the designated toilet spot every couple of hours and especially first thing in the morning, last thing at night, after meals and after play time.  Once the puppy is at the designated spot stay with them, when they go to the toilet heavily praise them both verbally and with a high quality food reward.  If your puppy does not go to the toilet take them back inside and continue every couple of hours. </p>
 
<p>This may sound like hard work but it will pay off as soon as your puppy learns that going to the toilet in the right place is a heavily rewarded activity.</p>
 
<h4>Top Toilet Training Tips</h4>

<ul>
<li>Remember your puppy has no built in understanding of where they  should or shouldn't go to the toilet.  You need to teach them.</li>
 
<li>Patience and persistence, toilet training will not happen over night and accidents will happen.</li>
 
<li>Reward, reward, reward - after putting in all this effort make sure your  puppy really knows what a good job they have done, this will reinforce the behaviour every time.</li>
 
<li>Keep the designated toilet area clean, pick up faeces and wash down urine, dogs will look for an alternative space if the area is dirty.</li>
 
<li>Take your puppy out to the designated toilet spot at regular intervals.  Good times include first thing in the morning, after meals, after sleeping and after play, you will soon start to learn your puppy's behaviour.</li>
 
<li>If your puppy goes to the toilet in the wrong place refrain from shouting at them as this only reinforces the behaviour and can break the trust you have built with your new puppy.  Calmly and without talking to them pick them up and take them to the designated spot, if they go again praise and reward them.</li>
 
<li>If accidents happen inside the house ensure you clean them up well and remove any odours.  A natural solution for eliminating urine is 50/50 white vinegar and water.  You can also purchase products from your local pet store.</li>
 
<li>If accidents persist after 4 months of age, seek further advice from your Veterinarian.  Some medical problems can cause lack of bladder control.</li>
</ul>
		
			
